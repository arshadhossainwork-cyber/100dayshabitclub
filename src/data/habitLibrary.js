/** Look up a habit by its URL slug */
export function getHabitBySlug(slug) {
  return habits.find((h) => h.slug === slug) || null;
}

/** Get all habits for a given category id */
export function getHabitsByCategory(categoryId) {
  return habits.filter((h) => h.category === categoryId);
}

/** Get the category object for a habit */
export function getCategoryForHabit(habit) {
  return HABIT_CATEGORIES.find((c) => c.id === habit.category) || null;
}

/** Get related habit objects for a habit */
export function getRelatedHabits(habit) {
  return habit.related
    .map((slug) => habits.find((h) => h.slug === slug))
    .filter(Boolean);
}
